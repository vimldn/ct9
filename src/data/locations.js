// Helper functions
export function getLocationBySlug(slug) {
  return locations.find(location => location.slug === slug);
}

export function getAllLocationSlugs() {
  return locations.map(location => location.slug);
}

export function getAllLocations() {
  return locations;
}

export function getLocationsByBorough(borough) {
  return locations.filter(location => location.borough === borough);
}

export function getLocationsByRegion(region) {
  return locations.filter(location => location.region === region);
}

export function getBoroughs() {
  return locations.filter(location => location.isBorough);
}

export function getRegions() {
  return [...new Set(locations.map(location => location.region))];
}

export function getNeighbourhoods() {
  return locations.filter(location => !location.isBorough);
}

